package com.sanker.entity.game;

import java.io.Serializable;

import com.google.gson.annotations.Expose;

/**
 * 战绩流水
 * @author 滕洁
 * @date 2016-11-25
 */
public class Ledger implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	@Expose
	private String id;

	@Expose
	private String playerId;
	
	/**
	 * 流水类型  收入 0/支出 1
	 */
	@Expose
	private String ledgerType;
	
	/**
	 * 倍数 / 番数
	 */
	@Expose
	private int ledgerNum;
	
	/**
	 * 分数
	 */
	@Expose
	private int ledgerScore;
	
	/**
	 * 流水对象
	 */
	@Expose
	private String target;
	
	/**
	 * 流水明细
	 */
	@Expose
	private String ledgerDetail;
	
	/**
	 * 流水名称
	 * @return
	 */
	@Expose
	private String ledgerName;
	
	/**
	 * 好友房中当前局数
	 * @return
	 */
	private Integer gameNum;
	
	/**
	 * 房间ID
	 * @return
	 */
	private String roomId;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPlayerId() {
		return playerId;
	}

	public void setPlayerId(String playerId) {
		this.playerId = playerId;
	}

	public String getLedgerType() {
		return ledgerType;
	}

	public void setLedgerType(String ledgerType) {
		this.ledgerType = ledgerType;
	}

	public int getLedgerNum() {
		return ledgerNum;
	}

	public void setLedgerNum(int ledgerNum) {
		this.ledgerNum = ledgerNum;
	}

	public int getLedgerScore() {
		return ledgerScore;
	}

	public void setLedgerScore(int ledgerScore) {
		this.ledgerScore = ledgerScore;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public String getLedgerDetail() {
		return ledgerDetail;
	}

	public void setLedgerDetail(String ledgerDetail) {
		this.ledgerDetail = ledgerDetail;
	}

	public String getLedgerName() {
		return ledgerName;
	}

	public void setLedgerName(String ledgerName) {
		this.ledgerName = ledgerName;
	}

	public Integer getGameNum() {
		return gameNum;
	}

	public void setGameNum(Integer gameNum) {
		this.gameNum = gameNum;
	}

	public String getRoomId() {
		return roomId;
	}

	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}
}
